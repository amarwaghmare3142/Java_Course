public class increment_decrement {
    public static void main(String[] args) {
//Increment Operators
     int a=24;
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);

//Decrement Operators
     int b=99;
        System.out.println(b--);
        System.out.println(b);
        System.out.println(--b);

//Quick Quiz
     int y=7;
     int x=(++y)*8;
        System.out.println(x);

//For Characters
     char ch= 'a';
        System.out.println(++ch);
    }
}
