public class aw_23_for_loops {
    public static void main(String[] args) {
        for (int i=5; i<=10; i++){
            System.out.println(i);
            }
        // For first n odd numbers
        System.out.println("First 15 odd natural numbers");
        for(int i=0; i<15; i++){
            System.out.println(2*i+1);
        }

        System.out.println("First 10 natural numbers in reverse formal");
        for(int i=10; i>=1; i--){
            System.out.println(i);
        }
    }
}

