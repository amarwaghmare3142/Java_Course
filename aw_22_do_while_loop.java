public class aw_22_do_while_loop {
    public static void main(String[] args) {
        System.out.println("From number 5");
        int a = 5;
        do {
            System.out.println(a);
            a++;
        }while(a<=15);

        System.out.println("Natural numbers");
        int b = 1;
        do {
            System.out.println(b);
            b++;
        }while(b<=15);
    }
}
